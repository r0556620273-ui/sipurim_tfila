def _clean(text: str) -> str:
    text = re.sub(r'<[^>]+>', '', text)
    text = _html.unescape(text)
    return ' '.join(text.split()).strip()


def _excerpt(text: str, max_chars: int = 390) -> str:
    if len(text) <= max_chars:
        return text
    cut = text.rfind('.', 0, max_chars)
    if cut == -1:
        cut = max_chars
    return text[:cut + 1].strip()


def _segments_to_text(he: object, max_segs: int = 4) -> str:
    if isinstance(he, str):
        return _clean(he)
    if isinstance(he, list):
        parts: list[str] = []
        for item in he[:max_segs]:
            if isinstance(item, str):
                c = _clean(item)
                if c:
                    parts.append(c)
            elif isinstance(item, list):
                for seg in item[:max_segs]:
                    if isinstance(seg, str):
                        c = _clean(seg)
                        if c:
                            parts.append(c)
        return ' '.join(parts)
    return ''


def _fetch_sefaria(ref: str, max_segs: int = 4) -> Optional[str]:
    try:
        encoded = requests.utils.quote(ref, safe='')
        url = f"{SEFARIA_BASE}/{encoded}?lang=he&commentary=0&context=0"
        r = requests.get(url, timeout=TIMEOUT)
        r.raise_for_status()
        data = r.json()
        if data.get('error'):
            return None
        text = _segments_to_text(data.get('he', ''), max_segs)
        return _excerpt(text) if text else None
    except Exception:
        return None


def _get_story_content(story: dict) -> tuple[str, str, str]:
    """מחזיר (title, text, source) — מביא מ-Sefaria אם צריך."""
    if "sefaria_ref" in story:
        text = _fetch_sefaria(story["sefaria_ref"], max_segs=3)
        return story["title"], text or "לא ניתן לטעון טקסט.", story["source"]
    return story["title"], story["text"], story["source"]


def _get_hebrew_date(d: date) -> str:
    try:
        url = (
            f"{HEBCAL_BASE}?cfg=json"
            f"&gy={d.year}&gm={d.month}&gd={d.day}&g2h=1"
        )
        r = requests.get(url, timeout=TIMEOUT)
        r.raise_for_status()
        return r.json().get('hebrew', '') or d.strftime('%d/%m/%Y')
    except Exception:
        return d.strftime('%d/%m/%Y')


