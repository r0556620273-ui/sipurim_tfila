def _build_quote_pool() -> list[dict]:
    pool: list[dict] = []

    # 1. שולחן ערוך אורח חיים — סימנים 46–215 ו-232–241
    for siman in list(range(46, 216)) + list(range(232, 242)):
        topic, section = _sa_oc_meta(siman)
        pool.append({
            "ref":        f"Shulchan Aruch, Orach Chaim {siman}:1",
            "label":      f'שו"ע אורח חיים, סי\' {siman}',
            "topic":      topic,
            "section":    section,
            "highlights": [],
        })

    # 2. משנה ברכות — כל המשניות
    for ch, count, topic, section in _MISHNA_DATA:
        for m in range(1, count + 1):
            pool.append({
                "ref":        f"Mishnah Berakhot {ch}:{m}",
                "label":      f"משנה ברכות, פרק {ch} משנה {m}",
                "topic":      topic,
                "section":    section,
                "highlights": [],
            })

    # 3. תלמוד בבלי ברכות — דפים ב–סד, שני הצדדים
    for daf in range(2, 65):
        for side, side_he in [("a", 'ע"א'), ("b", 'ע"ב')]:
            topic, section = next(
                (v for r, v in _BERAKHOT_META.items() if daf in r),
                ("תפילה", "כלל התפילה"),
            )
            pool.append({
                "ref":        f"Berakhot {daf}{side}",
                "label":      f"תלמוד בבלי, ברכות דף {daf} {side_he}",
                "topic":      topic,
                "section":    section,
                "highlights": [],
            })

    # 4. רמב"ם הלכות תפילה — פרק א–טו, שתי הלכות ראשונות בכל פרק
    for ch, (topic, section) in enumerate(_RAMBAM_CHAPTERS, 1):
        for h in range(1, 3):
            pool.append({
                "ref":        f"Mishneh Torah, Laws of Prayer and the Priestly Blessing {ch}:{h}",
                "label":      f"רמב\"ם, הלכות תפילה, פרק {ch} הלכה {h}",
                "topic":      topic,
                "section":    section,
                "highlights": [],
            })

    return pool


_QUOTE_POOL = _build_quote_pool()   # 393 ערכים


