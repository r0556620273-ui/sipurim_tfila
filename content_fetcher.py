def get_daily_entry():
    """
    מחזיר PrayerEntry עם תוכן יומי ממקורות אמינים בלבד.
    ציטוט + סיפור שונים בכל יום — אין חזרה לשנה שלמה ויותר.
    """
    from tefilah_newsletter import PrayerEntry

    today   = date.today()
    day_num = (today - EPOCH).days   # ימים מאז 1.1.2026

    # ── ציטוט (393 מקורות, מחזור 393 יום) ──────────────────
    q_meta = _QUOTE_POOL[day_num % len(_QUOTE_POOL)]
    q_text = _fetch_sefaria(q_meta['ref'])

    if not q_text:
        fallback = _QUOTE_POOL[(day_num + 1) % len(_QUOTE_POOL)]
        q_text   = _fetch_sefaria(fallback['ref'])
        if q_text:
            q_meta = fallback

    if not q_text:
        q_text = "לא ניתן לטעון ציטוט — אנא בדוק חיבור לאינטרנט."

    # ── סיפור (220 מקורות, offset שונה — לא מסונכרן עם הציטוט)
    story_meta = _STORY_POOL[(day_num + 137) % len(_STORY_POOL)]
    s_title, s_text, s_source = _get_story_content(story_meta)

    # ── תאריך עברי ──────────────────────────────────────────
    hebrew_date = _get_hebrew_date(today)

    return PrayerEntry(
        date            = today.strftime('%d/%m/%Y'),
        hebrew_date     = hebrew_date,
        topic           = q_meta['topic'],
        tefilla_section = q_meta['section'],
        quote           = q_text,
        quote_source    = q_meta['label'],
        story_title     = s_title,
        story           = s_text,
        story_source    = s_source,
        highlight_words = q_meta['highlights'],
    )
